	public void SetDesignWorld() {
		int r = infomap.GetRow();
		int c = infomap.GetCol();
		int[][] temp = new int[r][c];
		if(!design) {
			for(int i = 0;i < r;i++) {
				for(int j = 0;j < c;j++) {
					temp[i][j] = 0;
					tempmap[i][j] = 0;
					infomap.SetCellTime(i, j, 0);
				}
			}
			design = true;
			infomap.SetMap(temp);
			timer.stop();
			this.repaint();
		}else {
			design = false;
			infomap.SetMap(tempmap);
			timer.restart();
		}
	}
	
