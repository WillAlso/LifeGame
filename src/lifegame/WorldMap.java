	private int NeighborCellNum(int row, int col) {
        int cnt = 0;
        int[][] map = infomap.GetMap();
        for (int i = row - 1; i <= row + 1; i++) {
            for (int j = col - 1; j <= col + 1; j++) {
                if (i < 0 || i >= map.length || j < 0 || j >= map[0].length) {
                    continue;
                }
                if (map[i][j] == 1) {
                    cnt++;
                }
            }
        }
        if (map[row][col] == 1) {
            cnt--;  
        }
        return cnt;
    }  
	
	private void ChangeCellStatus() {
		int cnt = 0;
        for (int row = 0; row < tempmap.length; row++) {
            for (int col = 0; col < tempmap[row].length; col++) {  
  
                switch (NeighborCellNum(row, col)) {  
                case 0:  
                case 1:  
                case 4:  
                case 5:  
                case 6:  
                case 7:  
                case 8:  
                	tempmap[row][col] = 0;
                    break;  
                case 2:  
                	tempmap[row][col] = infomap.GetMap()[row][col];  
                    break;  
                case 3:  
                	tempmap[row][col] = 1;  
                    break;  
                }  
                if(tempmap[row][col] != infomap.GetMap()[row][col]) {
                	cnt++;
                	infomap.SetCellTime(row, col, 0);
                }else {
                	if(tempmap[row][col] == 1) {
                		infomap.SetCellTime(row, col, infomap.GetTime()[row][col] + 1);
                	}else {
                		infomap.SetCellTime(row, col, 0);
                	}
                }
            }  
        }
        infomap.SetMap(tempmap);
        changenum = cnt;
	}
	
	protected void paintComponent(Graphics g) {
        super.paintComponent(g);
        int[][] map = infomap.GetMap();
        int width = infomap.GetWidth();
        int height = infomap.GetHeight();
        for (int i = 0; i < map.length; i++) {
            for (int j = 0; j < map[i].length; j++) {
                if (map[i][j] == 1) {
                	g.fillRect( j * width, i * height, width, height);
                } else {
                	g.drawRect( j * width, i * height, width, height);
                }
            }
        }
        if(click) {
        	g.setColor(new Color(255,0,0));
        	g.drawRect( click_x * width, click_y * height, width, height);
        	g.setColor(new Color(0,0,0));
        }
        int livenum = infomap.GetLivenum();
        String t1 = "活细胞数目 " + String.valueOf(livenum);
        String t2 = "改变状态数  " + String.valueOf(changenum);
        String t5 = "总   周   期  " + String.valueOf(total_time);
        label5.setBounds(worldsize_w - 190, 10, t5.length() * 15, 20);
        label5.setText(t5);
        label0.setBounds(worldsize_w - 190, 30, 100, 20);
        label1.setBounds(worldsize_w - 190, 45, t1.length() * 15, 20);
        label2.setBounds(worldsize_w - 190, 60, t2.length() * 15, 20);
        label2.setText(t2);
        label1.setText(t1);
        if(changenum == 0) {
        	label0.setText("状        态    " + "稳定");
        }else {
        	label0.setText("状        态    " + "变化");
        }
        if(click) {
        	if(map[click_y][click_x] == 1) {
        		label3.setText("选中细胞状态 活");
        		String t4 = "存活        周期  " + String.valueOf(infomap.GetTime()[click_y][click_x] + 1);
        		label4.setText(t4);
        		label3.setBounds(worldsize_w - 190, 90, 100, 20);
        		label4.setBounds(worldsize_w - 190, 105, t4.length() * 15, 20);
        		
        	}else {
        		label3.setText("选中细胞状态 死");
        		label3.setBounds(worldsize_w - 190, 90, 100, 20);
        		label4.setText("");
        	}
        }
    }

